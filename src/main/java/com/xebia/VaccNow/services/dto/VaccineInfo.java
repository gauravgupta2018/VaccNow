package com.xebia.VaccNow.services.dto;

public class VaccineInfo {

	private String branchCode;
	private String vaccineName;
	private String vaccineCode;
	private String availabiltyCount;
	
	public String getBranchCode() {
		return branchCode;
	}
	public void setBranchCode(String branchCode) {
		this.branchCode = branchCode;
	}
	public String getVaccineName() {
		return vaccineName;
	}
	public void setVaccineName(String vaccineName) {
		this.vaccineName = vaccineName;
	}
	public String getVaccineCode() {
		return vaccineCode;
	}
	public void setVaccineCode(String vaccineCode) {
		this.vaccineCode = vaccineCode;
	}
	public String getAvailabiltyCount() {
		return availabiltyCount;
	}
	public void setAvailabiltyCount(String availabiltyCount) {
		this.availabiltyCount = availabiltyCount;
	}
	
	
	
}
